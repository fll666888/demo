    /**
     * 向指定 URL 发送POST上传文件方法的请求
     *
     * @param url
     *            发送请求的 URL
     * @param file
     *            上传文件
     * @return 所代表远程资源的响应结果
     */
    public static String sendPost_upload(String url, String param, MultipartFile file) {
        PrintWriter out = null;
        BufferedReader in = null;
        String result = "";
        // 换行符
        final String newLine = "\r\n";
        final String boundaryPrefix = "--";
        // 定义数据分隔线
        String BOUNDARY = "========7d4a6d158c9";
        try {
            URL realUrl = new URL(url);
            // 打开和URL之间的连接
            URLConnection conn = realUrl.openConnection();
            // 设置通用的请求属性
            conn.setRequestProperty("accept", "*/*");
            conn.setRequestProperty("connection", "Keep-Alive");
            conn.setRequestProperty("user-agent",
                    "Mozilla/4.0 (compatible; MSIE 6.0; Windows NT 5.1;SV1)");
            conn.setRequestProperty("Content-Type", "multipart/form-data; boundary=" + BOUNDARY); // 设置发送数据的格式
            // 发送POST请求必须设置如下两行
            conn.setDoOutput(true);
            conn.setDoInput(true);

            // 传输内容
            StringBuffer contentBody = new StringBuffer();
            //输出
            OutputStream outputStream = conn.getOutputStream();
            //处理file文件的POST请求（多个file可以循环处理）
            contentBody.append("--").append(BOUNDARY)
                    .append("\r\n")
                    .append("Content-Disposition:form-data; name=\"")
                    .append("flag" + "\"")
                    .append("\r\n")
                    .append("\r\n")
                    .append(param)
                    .append("\r\n")
                    .append("--").append(BOUNDARY)
                    .append("\r\n")
                    .append("Content-Disposition:form-data; name=\"")
                    .append("file" +"\"; ")   // form中field的名称
                    .append("filename=\"")
                    .append(file.getOriginalFilename() +"\"")   //上传文件的文件名，包括目录
                    .append("\r\n")
                    .append("Content-Type:multipart/form-data")
                    .append("\r\n\r\n");

            String boundaryMessage = contentBody.toString();
            // 将参数头的数据写入到输出流中
            outputStream.write(boundaryMessage.getBytes("utf-8"));
            // 输入流,用于读取文件数据
            DataInputStream dis = new DataInputStream(file.getInputStream());
            byte[] bufferOut = new byte[1024];
            int bytes = 0;
            // 每次读1KB数据,并且将文件数据写入到输出流中
            while ((bytes = dis.read(bufferOut)) != -1) {
                outputStream.write(bufferOut, 0, bytes);
            }
            // 最后添加换行（多个文件时添加）
            outputStream.write(newLine.getBytes());
            dis.close();
            // 定义最后数据分隔线，即--加上BOUNDARY再加上--。
            byte[] endData = ("\r\n--" + BOUNDARY + "--\r\n").getBytes();
            // 写上结尾标识
            outputStream.write(endData);

            // 获取URLConnection对象对应的输出流
            out = new PrintWriter(outputStream);
            // flush输出流的缓冲
            out.flush();
            out.close();
            // 定义BufferedReader输入流来读取URL的响应
            in = new BufferedReader(
                    new InputStreamReader(conn.getInputStream(), "utf-8"));
            String line;
            while ((line = in.readLine()) != null) {
                result += line;
            }
        } catch (Exception e) {
            System.out.println("发送 POST 请求出现异常！"+e);
            e.printStackTrace();
        }
        //使用finally块来关闭输出流、输入流
        finally{
            try{
                if(out!=null){
                    out.close();
                }
                if(in!=null){
                    in.close();
                }
            }
            catch(IOException ex){
                ex.printStackTrace();
            }
        }
        return result;
    }

